namespace Eresus\CmsBundle\Extensions;

use Symfony\Component\HttpKernel\Bundle\Bundle;

/**
 * Родительский класс для пакетов всех плагинов
 *
 * @package Eresus
 * @since 4.00
 */
class PluginBundle extends Bundle
{
    /**
     * Конструктор
     * @since 4.00
     */
    public function __construct()
    {
        /*
         * Мы не можем переопределить метод getName, т. к. он объявлен как final. Но есть выход —
         * мы можем сразу задать имя в $this->name, и родительский getName возьмёт его оттуда.
         */
        $this->name = str_replace('\\', '', get_class($this));
    }
}

